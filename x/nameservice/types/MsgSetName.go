package types

import (
	"fmt"
	sdk "github.com/cosmos/cosmos-sdk/types"
	sdkerrors "github.com/cosmos/cosmos-sdk/types/errors"
)

// MsgSetName defines a SetName message
type MsgSetName struct {
	Name    string         `json:"name"`
	Value   string         `json:"value"`
	Creator sdk.AccAddress `json:"creator"`
}

func (msg MsgSetName) Reset() {
	msg = MsgSetName{}
}

func (msg MsgSetName) String() string {
	return fmt.Sprintf(`MsgSetName:
	  Name:       	%s
	  Value: 		%s
	  Creator:      %v
	`, msg.Name, msg.Value, msg.Creator)
}

func (msg MsgSetName) ProtoMessage() {}

// NewMsgSetName is a constructor function for MsgSetName
func NewMsgSetName(name string, value string, creator sdk.AccAddress) MsgSetName {
	return MsgSetName{
		Name:    name,
		Value:   value,
		Creator: creator,
	}
}

// Route should return the name of the module
func (msg MsgSetName) Route() string { return RouterKey }

// Type should return the action
func (msg MsgSetName) Type() string { return "set_name" }

// ValidateBasic runs stateless checks on the message
func (msg MsgSetName) ValidateBasic() error {
	if msg.Creator.Empty() {
		return sdkerrors.Wrap(sdkerrors.ErrInvalidAddress, msg.Creator.String())
	}
	if len(msg.Name) == 0 || len(msg.Value) == 0 {
		return sdkerrors.Wrap(sdkerrors.ErrUnknownRequest, "Name and/or Value cannot be empty")
	}
	return nil
}

// GetSignBytes encodes the message for signing
func (msg MsgSetName) GetSignBytes() []byte {
	return sdk.MustSortJSON(Amino.MustMarshalJSON(&msg))
}

// GetSigners defines whose signature is required
func (msg MsgSetName) GetSigners() []sdk.AccAddress {
	return []sdk.AccAddress{msg.Creator}
}
